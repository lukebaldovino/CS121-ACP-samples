a = 18
b = 6

is_equal = (a == b)
is_greater = (a > b)
is_lesser = (a < b)
is_not_zero = (a != 0)

print(f"{a} == {b}: {is_equal}")    # 18 == 6: False
print(f"{a} > {b}: {is_greater}")   # 18 > 6: True
print(f"{a} < {b}: {is_lesser}")    # 18 < 6: False
print(f"{a} != 0: {is_not_zero}")   # 18 != 0: True
