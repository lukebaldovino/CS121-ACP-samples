class Triangle:
    def __init__(self, base=0, height=0):
        self.base = base
        self.height = height

# Create a triangle with default base and height (0)
rect1 = Triangle()

# Create a triangle with base 8 and default height (0)
rect2 = Triangle(8)

# Create a triangle with base 12 and height 4
rect3 = Triangle(12, 4)

