##########################################################
#                  BITWISE OPERATORS                     #
##########################################################

x = 6 # binary: 110
y = 3 # binary: 011

# Bitwise AND
and_result = x & y    # result: 010 output: 2

# Bitwise OR
or_result = x | y    # result: 111 output: 7

# Bitwise XOR
xor_result = x ^ y   # result: 101 output: 5

# Bitwise NOT
not_result = ~x      # result: -111 output: -7

# Left Shift
left_shift = x << 1  # result: 1100 output: 12

# Right Shift
right_shift = x >> 1 # result: 011 output: 3

