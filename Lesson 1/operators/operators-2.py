num1 = 14
num2 = 7

num1 += num2  # Similar to num1 = num1 + num2
print("num1 += num2:", num1)

num1 -= num2  # Similar to num1 = num1 - num2
print("num1 -= num2:", num1)

num1 *= num2  # Similar to num1 = num1 * num2
print("num1 *= num2:", num1)

num1 /= num2  # Similar to num1 = num1 / num2
print("num1 /= num2:", num1)

num1 //= num2  # Similar to num1 = num1 // num2
print("num1 //= num2:", num1)

num1 %= num2  # Similar to num1 = num1 % num2
print("num1 %=", num1)